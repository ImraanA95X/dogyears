const myAge = 20; // My age
let earlyYears = 2; // First two human years
earlyYears *= 10.5;
let laterYears = myAge - 2; // First two human years are accounted for
laterYears *= 4; // Multiply the remaining human years in age by 4
let myAgeInDogYears = earlyYears + laterYears; // Total age in dog years
let myName = 'John';
'John'.toLowerCase(); // makes all characters lower case
console.log(`My name is ${myName}. I am ${myAgeInDogYears} years old in dog years.`); // displays name and age in dog years